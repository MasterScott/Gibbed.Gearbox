using System;
using System.Collections.Generic;
using System.Globalization;
using System.Linq;
using System.Windows;
using System.Windows.Controls;
using System.Windows.Data;

namespace Gibbed.Gearbox.WPF
{
    public class ContainsToVisiblityConverter : IMultiValueConverter
    {
        public object Convert(object[] values, Type targetType, object parameter, CultureInfo culture)
        {
            if (values == null || values.Length != 2)
            {
                return Visibility.Collapsed;
            }

            if (values[0] is IEnumerable<string> strings)
            {
                var value = values[1];
                if (strings.Contains(value) == true)
                {
                    return Visibility.Collapsed;
                }
            }
            else if (values[0] is ItemCollection collection)
            {
                var value = values[1];
                if (collection.Contains(value) == true)
                {
                    return Visibility.Collapsed;
                }
            }

            return Visibility.Visible;
        }

        public object[] ConvertBack(object value, Type[] targetTypes, object parameter, CultureInfo culture)
        {
            throw new NotSupportedException();
        }
    }
}
